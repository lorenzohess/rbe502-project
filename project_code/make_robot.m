function [robot, qlim] = make_robot()
    off = atan2(0.024, 0.128) - pi/2;
    robot = SerialLink([ ...
        Revolute('a', 0,     'd', 0.077, 'alpha', pi/2), ...
        Revolute('a', 0.130, 'd', 0,     'alpha', 0, 'offset', -off), ...
        Revolute('a', 0.124, 'd', 0,     'alpha', 0, 'offset',  off), ...
        Revolute('a', 0.126, 'd', 0,     'alpha', 0)], ...
        'name', 'OpenManipulator-X');
    qlim = [-180 180; -103 90; -54 79; -102 117] .* pi/180;
end
